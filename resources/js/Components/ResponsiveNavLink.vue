        : 'block w-full ps-3 pe-4 py-2 border-l-4 border-transparent text-start text-base font-medium text-white hover:text-orange-500 hover:bg-gray-500 hover:border-gray-300 focus:outline-none focus:text-gray-800 focus:bg-gray-500 focus:border-red-300 transition duration-150 ease-in-out'
);
</script>

<template>
    <Link :href="href" :class="classes">
        <slot />
    </Link>
</template>
